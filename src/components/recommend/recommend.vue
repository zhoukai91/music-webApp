<template>
  <div class="recommend" ref="recommend">
    <Scroll ref="scroll" class="recommend-content">
      <div>
        <div v-if="recommends.length" class="slider-wrapper">
          <slider>
            <div v-for="item in recommends" :key="item.id">
              <a :href="item.linkUrl">
                <img class="needsclick" :src="item.picUrl">
              </a>
            </div>
          </slider>
        </div>
        <div class="recommend-list">
          <h1 class="list-title">热门歌单推荐</h1>
          <ul>
            <li class="item" v-for="item in discList" :key="item.mv_id">
              <div class="icon">
                <img width="60px" height="60px" v-lazy="item.picurl" alt="">
              </div>
              <div class="text">
                <h2 class="name">{{item.mvtitle}}</h2>
                <p class="desc">{{item.mvdesc}}</p>
              </div>
            </li>
          </ul>
        </div>
        <div class="loading-wrapper" v-if="!discList.length">
          <Loading></Loading>
        </div>
      </div>
    </Scroll>
  </div>
</template>

<script type="text/ecmascript-6">
import {getRecommend, getDiscList} from 'api/recommend'
import {ERR_OK} from 'api/config'
import Slider from 'components/base/slider/slider'
import Scroll from 'components/base/scroll/scroll'
import Loading from 'components/base/loading/loading'

export default {
  name: 'recommend',
  data () {
    return {
      recommends: [],
      discList: []
    }
  },
  created () {
    this._getRecommend()
    this._getDiscList()
  },
  methods: {
    _getRecommend () {
      getRecommend().then((res) => {
        if (res.code === ERR_OK) {
          this.recommends = res.data.slider
        }
      })
    },
    _getDiscList () {
      getDiscList().then((res) => {
        if (res.code === ERR_OK) {
          this.discList = res.data.mvlist
        }
      })
    }
  },
  components: {
    Slider,
    Scroll,
    Loading
  }
}
</script>

